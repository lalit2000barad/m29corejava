//program to demonstrate on string built in function
package org.tnsif.stringsprogram;
public class StringOperationExample {

	public static void main(String[] args) {
		//using new keyword
		String str1=new String("Aishwarya");
		System.out.println(str1.toUpperCase());
		System.out.println(str1.length());
		System.out.println(str1.lastIndexOf('a'));
		System.out.println(str1.hashCode());
		System.out.println(str1.concat("Shiwani"));
		System.out.println(str1+"Shinde");
		
		
		
		
		
		
		
	}

}